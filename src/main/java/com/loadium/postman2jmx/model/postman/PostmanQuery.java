package com.loadium.postman2jmx.model.postman;

public class PostmanQuery {
    private String key;
    private String value;


    public PostmanQuery() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
